<?php

namespace CodeShopping\Events;

use CodeShopping\Models\User;

class UserCreatedEvent
{
    /**
     * @var User
     */
    private $user;

    public function __construct(User $user)
    {
        $this->user = $user;
    }

    /**
     * @return User
     */
    public function getUser(): User
    {
        return $this->user;
    }
}
